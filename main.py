@app.route('/test/<category>/<grade>', methods=['GET', 'POST'])
def test(category, grade=None):
    if category == 'iq':
        questions = iq_questions
        test_name = "IQ –¢–µ—Å—Ç"
    elif category == 'school' and grade:
        questions = school_questions.get(grade, [])
        test_name = f"{grade} –∫–ª–∞—Å—Å"
    else:
        return redirect(url_for('index'))

    if request.method == 'POST':
        score = 0
        for i, q in enumerate(questions):
            user_answer = request.form.get(f'q{i}')
            if user_answer == q['answer']:
                score += 1

        percent = round(score / len(questions) * 100) if questions else 0

        if percent <= 30: iq_estimate = 70
        elif percent <= 50: iq_estimate = 85
        elif percent <= 65: iq_estimate = 100
        elif percent <= 80: iq_estimate = 115
        elif percent <= 90: iq_estimate = 130
        else: iq_estimate = 145

        return render_template(
            'result.html',
            score=score,
            total=len(questions),
            percent=percent,
            test_name=test_name,
            iq_estimate=iq_estimate
        )

    return render_template('test.html', questions=questions)

